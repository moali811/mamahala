export interface Customer {
  /** Primary key — lowercase email. */
  email: string;
  name: string;
  /** ISO-2 country code (uppercase). */
  country: string;
  phone?: string;
  /** Optional second phone (distinct from `phone`). Zoho-import populates this from Phone column when MobilePhone is primary. */
  mobilePhone?: string;
  /** Professional salutation (e.g., 'Mrs.', 'Mr.', 'Dr.'). */
  salutation?: string;
  /** Individual or business account type. Default: 'individual'. */
  customerType?: 'individual' | 'business';
  /** For business customers, the company name. For individuals, optional relationship metadata. */
  companyName?: string;
  /** Zoho Books Customer ID — used for cross-referencing imported invoices to imported customers. */
  zohoCustomerId?: string;
  /**
   * 2-4 letter initials used in invoice number generation. Computed from
   * `name` at first-write time and persisted so future invoices for this
   * customer reuse the same prefix. Collision-safe: if two customers share
   * 2-letter initials, both get upgraded to 3-letter.
   * Example: `HA` for "Hala Ali", `HLA` if collision with "Hadia Ahmed".
   */
  effectiveInitials?: string;
  /**
   * Next sequential invoice counter for this customer. Starts at 1 and
   * increments atomically on each new invoice. Feeds into the invoice
   * number: `MHC-YYYYMM-{effectiveInitials}-{nextInvoiceSeq}`.
   */
  nextInvoiceSeq?: number;
  address?: {
    line1?: string;
    line2?: string;
    city?: string;
    state?: string;
    postal_code?: string;
  };
  /** Admin's free-text notes about the client. */
  notes?: string;
  /** Tags for filtering / segmentation. */
  tags?: string[];
  /** Stored display currency from previous invoices. */
  preferredCurrency?: string;
  /** Most-used payment method on past invoices. */
  preferredPaymentMethod?: PaymentMethodRecord;
  /* Aggregated stats — denormalized for fast list rendering */
  totalInvoices: number;
  totalPaidCAD: number;
  outstandingCAD: number;
  firstInvoiceAt?: string;
  lastInvoiceAt?: string;
  /* AI insights cache (refreshed on demand or weekly) */
  aiInsightSummary?: string;
  aiInsightTags?: string[];
  aiInsightUpdatedAt?: string;
  /* Lifecycle */
  createdAt: string;
  updatedAt: string;
  /** Where this record came from. */
  source: 'manual' | 'csv-import' | 'auto-from-invoice';
}

/* ════════════════════════════════════════════════════════════
   Recurring Schedules
   ════════════════════════════════════════════════════════════ */

export type RecurringCadence = 'monthly' | 'weekly';

export interface RecurringSchedule {
  scheduleId: string;
  /** The client this schedule bills. */
  customerEmail: string;
  /** Snapshot of the draft used to spawn each instance. */
  templateDraft: InvoiceDraft;
  /** How often it runs. */
  cadence: RecurringCadence;
  /**
   * For monthly: day-of-month (1-28, capped to avoid Feb edge cases).
   * For weekly: day-of-week (0=Sunday … 6=Saturday).
   */
  anchor: number;
  /** First run (ISO date). */
  startDate: string;
  /** Optional end (ISO date). When passed, schedule auto-deactivates. */
  endDate?: string;
  /** Pause/resume toggle. */
  active: boolean;
  /** When TRUE, generated invoices are sent immediately; when FALSE, left as drafts. */
  autoSend: boolean;
  /**
   * IANA timezone the anchor is interpreted in (e.g. "America/Toronto").
   * Optional for backwards compatibility — legacy schedules without a
   * timezone are treated as UTC, matching the original behaviour. New
   * schedules should always set this so a "fire on the 15th" schedule
   * really fires on the local 15th instead of UTC 00:00 (which can be
   * the previous day in North American timezones).
   */
  timezone?: string;
  /* Telemetry */
  nextRunAt: string;
  lastRunAt?: string;
  totalRuns: number;
  /** All invoice IDs this schedule has spawned. */
  spawnedInvoiceIds: string[];
  /* Lifecycle */
  createdAt: string;
  updatedAt: string;
}


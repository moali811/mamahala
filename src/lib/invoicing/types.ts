/* ================================================================
   Invoice Composer — Shared TypeScript Types
   ================================================================
   Used by the rate breakdown, PDF generator, KV store, API routes,
   and admin UI components. Single source of truth for shape.
   ================================================================ */

import type { PricingTierKey, PricingBand } from '@/config/pricing';

export type ComplexityPreset =
  | 'standard'
  | 'plus25'
  | 'plus33'
  | 'plus50'
  | 'custom';

export type PackageKey = 'single' | 'pack3' | 'pack5' | 'pack10';

export type TaxMode = 'manual-hst' | 'none';

export type InvoiceStatus = 'draft' | 'sent' | 'paid' | 'overdue' | 'void';

export type PaymentMethodRecord =
  | 'e-transfer'
  | 'wire'
  | 'paypal'
  | 'stripe'
  | 'other'
  | 'unknown';

export interface ClientInput {
  name: string;
  email: string;
  /** ISO-3166-1 alpha-2, uppercase (e.g. 'CA', 'IN', 'AE'). */
  country: string;
  address?: {
    line1?: string;
    line2?: string;
    city?: string;
    state?: string;
    postal_code?: string;
  };
  phone?: string;
}

export interface InvoiceLineItem {
  /** Local UUID — used as React key. Regenerated each time a row is added. */
  id: string;
  /** Human-readable description of the item (shown on the invoice). */
  description: string;
  /** Quantity — usually 1 (session) but can be fractional hours. */
  quantity: number;
  /** Unit price in the draft's display currency. */
  unitPriceLocal: number;
  /**
   * Optional service slug for reporting categorization. Defaults to the
   * draft's primary `serviceSlug` when not set. Useful for multi-line
   * invoices that bundle several distinct services.
   */
  serviceSlug?: string;
}

export interface InvoiceDraft {
  /** Local UUID — separate from the invoice number assigned at send time. */
  draftId: string;
  client: ClientInput;
  serviceSlug: string;
  complexity: {
    preset: ComplexityPreset;
    percent: number; // 0, 0.25, 0.33, 0.50, or custom (clamped 0..1)
  };
  package: PackageKey;
  /** 0..0.40 downward adjustment for hardship. */
  slidingScalePercent: number;
  /** Optional currency override — if unset, derived from client.country. */
  displayCurrency?: string;
  taxMode: TaxMode;
  allowETransfer: boolean;
  adminNote?: string;
  descriptionOverride?: string;
  daysUntilDue: number;
  /**
   * Manual price override — when enabled, bypasses the auto pricing-engine
   * computation and uses the admin-typed per-session amount instead.
   * The reason field is required for audit transparency.
   */
  manualPrice?: {
    enabled: boolean;
    perSessionLocal: number;
    currency?: string; // optional override for the currency too
    reason?: string;
  };
  /**
   * Optional free-text subject/memo line — shown on the PDF and in the email.
   * Dr. Hala uses this for appointment reminders and per-invoice context.
   * Example: "Appointment reminder: Sunday, April 12 at 2:30pm"
   */
  subject?: string;
  /** When this draft was generated by a recurring schedule, link back. */
  recurringScheduleId?: string;
  /**
   * Optional issue date override as a YYYY-MM-DD string. When set, the
   * invoice is stamped with this date instead of `now`. Also drives the
   * invoice-number YYYYMM prefix (so MHC-202604-HA-3 stays consistent
   * with the issue month). The due date is computed as
   * `issueDate + daysUntilDue` when this is set.
   */
  issueDate?: string;
  /**
   * Optional override for the invoice number. When set, the server uses
   * this string verbatim instead of the auto-generated MHC-YYYYMM-{Init}-{N}
   * format. Does NOT bump the customer's nextInvoiceSeq counter — the next
   * auto-generated invoice for the same customer will use the expected seq.
   */
  invoiceNumberOverride?: string;
  /**
   * Per-invoice override of Settings.customerNotes. When set, the PDF and
   * email show this block instead of the default. Use to tailor specific
   * session notes without changing the global default.
   */
  customerNotesOverride?: string;
  /**
   * Per-invoice override of Settings.termsAndConditions. Array of bullet
   * strings. When set, the PDF T&C section shows these lines instead of
   * the defaults.
   */
  termsOverride?: string[];
  /**
   * Optional multi-line-item list. When populated (length > 0), the rate
   * breakdown sums each item's quantity × unit price and uses that as the
   * subtotal, bypassing the auto pricing-engine. Preserves Dr. Hala's
   * Zoho-era multi-line invoices like "Parenting Session + CC processing fees".
   * When empty or undefined, the single-service behavior applies.
   */
  lineItems?: InvoiceLineItem[];
  /**
   * Optional per-invoice Stripe Payment Link URL. When set, the payment
   * concierge page uses this as the "Pay with Card" destination instead of
   * creating a dynamic Checkout Session. Useful when STRIPE_SECRET_KEY is
   * unavailable (the admin creates a one-time Payment Link in the Stripe
   * dashboard and pastes it here). Empty/undefined → concierge falls back
   * to the generic "customer chooses what to pay" link with an amount hint.
   */
  stripePaymentLink?: string;
  /**
   * Client's preferred email language, copied from the originating Booking
   * (or manually selected by admin when composing standalone). Drives the
   * locale of the invoice + receipt emails. Defaults to 'en' when absent.
   */
  preferredLanguage?: 'en' | 'ar';
  /**
   * ISO timestamp of the linked session's start. When populated, the
   * send-invoice pipeline uses this as the invoice due date (defaulting
   * to "pay by the session") instead of `issueDate + daysUntilDue`.
   * Populated automatically for drafts generated from a Booking; absent
   * for standalone admin invoices (which keep the daysUntilDue fallback).
   */
  sessionStartTime?: string;
  createdAt: string;
  updatedAt: string;
}

export interface InvoiceRateBreakdown {
  tierKey: PricingTierKey;
  serviceSlug: string;
  country: string;
  band: PricingBand;

  /* Base rate (from pricing engine, floor-safe) */
  baseAmountLocal: number;
  baseAmountCAD: number;
  displayCurrency: string;
  floorApplied: boolean;

  /* Complexity step */
  complexityPercent: number;
  afterComplexityLocal: number;
  afterComplexityCAD: number;

  /* Package + sliding scale */
  sessions: number;
  packageDiscountPercent: number;
  slidingScalePercent: number;
  perSessionLocal: number;

  /* Subtotal (pre-tax) */
  subtotalLocal: number;
  subtotalCAD: number;

  /* Tax */
  taxMode: TaxMode;
  taxPercent: number;
  taxAmountLocal: number;
  taxAmountCAD: number;

  /* Grand total */
  totalLocal: number;
  totalCAD: number;

  /* Display helpers */
  formattedBase: string;
  formattedTotal: string;
  formattedTotalCAD: string;
  /** Human-readable one-line formula for transparency. */
  formulaLine: string;
  /** Optional non-blocking warning (e.g., currency auto-corrected). */
  warning?: string;
}

export interface StoredInvoice {
  /** Internal stable ID: inv_{uuid}. */
  invoiceId: string;
  /**
   * Human-readable invoice number. Format: `MHC-YYYYMM-{Initials}-{N}`
   * where `Initials` is derived from the customer name and `N` is the
   * per-customer sequential session counter. Legacy invoices from Phase 2
   * use the older `INV-YYYYMM-NNNN` scheme, and early-2026 invoices used
   * the typo'd `MCH-` prefix — migrated via the migrate-prefix endpoint.
   */
  invoiceNumber: string;
  /** Immutable snapshot of the draft at send time. */
  draft: InvoiceDraft;
  /** Immutable snapshot of the computed breakdown. */
  breakdown: InvoiceRateBreakdown;
  status: InvoiceStatus;
  /** ISO timestamp when the invoice was finalized and sent. */
  issuedAt: string;
  /** ISO timestamp (issuedAt + daysUntilDue). */
  dueDate: string;
  paidAt?: string;
  voidedAt?: string;
  paymentMethod: PaymentMethodRecord;
  createdAt: string;
  updatedAt: string;
  /** Email delivery tracking. */
  emailMessageId?: string;
  emailSentAt?: string;
  /** True if created in Dry Run mode (skips email send). */
  dryRun: boolean;
  /** Dynamic Stripe Checkout URL with the exact invoice amount. */
  stripeCheckoutUrl?: string;
  /**
   * Opaque token used by the payment concierge page to look up this invoice
   * without exposing the invoiceId. Generated at send time via crypto.randomUUID().
   * The email CTA links to `/pay/{paymentToken}` so clients can pay regardless
   * of whether dynamic Stripe sessions are configured.
   */
  paymentToken?: string;
  /**
   * Where this record came from. Defaults to `'native'` when omitted
   * (for backward compat with Phase 2 records).
   * - `native` — created via Compose tab
   * - `zoho-import` — imported from Zoho Books CSV history
   * - `manual-backfill` — inserted programmatically for migration
   */
  origin?: 'native' | 'zoho-import' | 'manual-backfill';
  /**
   * Original invoice number as it appeared in the source system
   * (e.g., Zoho's `MHC-00000746` or `TH-S1`). Searchable from History.
   */
  legacyInvoiceNumber?: string;
  /**
   * Exchange rate snapshot at the time of the original invoice. Used
   * for historical imports so the CAD equivalent reflects the rate
   * at the original issue date, not today's FX.
   */
  legacyExchangeRate?: number;
  /**
   * Preserved original item name from the source system (e.g.,
   * "Parenting Coaching Session"). Used for display fidelity when
   * the canonical `serviceSlug` doesn't match the original wording.
   */
  legacyItemName?: string;
}

export interface InvoiceSettings {
  /* ── Branding ── */
  /** Official business name (e.g. 'Mama Hala Consulting Group'). */
  businessName: string;
  /** Official company registration ID. */
  companyId: string;
  /** Public website URL (no trailing slash). */
  websiteUrl: string;
  /** Booking policy URL — included in T&C section as a clickable link. */
  bookingPolicyUrl: string;
  /** Default Customer Notes block (appears on every invoice). */
  customerNotes: string;
  /** Default Terms & Conditions block — array of bullet points. */
  termsAndConditions: string[];

  /* ── Tax + e-Transfer (Phase 1) ── */
  defaultTaxMode: TaxMode;
  defaultDaysUntilDue: number;
  defaultAllowETransfer: boolean;
  /** Where Canadian clients send Interac e-Transfer. */
  eTransferEmail: string;
  /** Optional wire transfer instructions (shown to non-CA clients). */
  wireInstructions?: string;
  /** Optional PayPal.me link (shown to non-CA clients). */
  paypalLink?: string;
  /**
   * Optional global-default Stripe Payment Link (e.g. a "customer chooses
   * what to pay" link from the Stripe dashboard). Used as the "Pay with
   * Card" destination on the concierge page when:
   *   1. STRIPE_SECRET_KEY is not configured (no dynamic session), AND
   *   2. The invoice draft has no per-invoice stripePaymentLink override.
   *
   * Paste once here and every invoice automatically gets a card button —
   * no more per-invoice pasting. Safe to leave empty (concierge will fall
   * back to e-Transfer / wire / PayPal only).
   */
  defaultStripePaymentLink?: string;
  /** Optional Canadian HST/GST registration number shown on invoice header. */
  hstNumber?: string;
  /** Issuer block shown in the invoice header. */
  issuerBlock: {
    name: string;
    line1: string;
    city: string;
    postalCode: string;
    country: string;
    email: string;
    phone: string;
  };

  /* ── Recurring defaults ── */
  /** When new recurring schedules default to auto-send (vs draft-only). */
  recurringAutoSendDefault: boolean;

  /* ── Dry Run mode ── */
  /** When ON, create invoice + generate PDF + store KV, but skip the email send. */
  dryRun: boolean;
  updatedAt: string;
}

